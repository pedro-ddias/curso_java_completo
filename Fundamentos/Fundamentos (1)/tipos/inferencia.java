package Fundamentos.Fundamentos.tipos;

public class inferencia {
   public static void main(String[] args) {
       
    // pelo valor que você colocou o java vai definir o tipo
    var b = 4.5; //declarou a variável e inicializou

    int a; //iniciou variável
    a = 4; //declarou variável

    System.out.println(b  + "," + a);

    //você não pode mudar o tipo da variável ao decorrer do programa
    
   }
}
