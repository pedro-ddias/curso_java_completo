package Fundamentos.Fundamentos.operadores;

public class operadoresUnarios {
    public static void main(String[] args) {
        
        int a = 1;
        int b = 2;

        a++; // incremento a = a +1
        b--; // decremento a = a -1

        System.out.println(a);
        System.out.println(b);
    }
}
